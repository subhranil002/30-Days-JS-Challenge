let numberVar = 42; // declare and assign a number to numberVar
let stringVar = "Hello, world!"; // declare and assign a string to stringVar
let booleanVar = true; // declare and assign a boolean to booleanVar
let objectVar = { name: "Subhranil", age: 21 }; // declare and assign an object to objectVar
let arrayVar = [1, 2, 3, 4, 5]; // declare and assign an array to arrayVar

console.log(numberVar, typeof numberVar); // 42 'number'
console.log(stringVar, typeof stringVar); // "Hello, world!" 'string'
console.log(booleanVar, typeof booleanVar); // true 'boolean'
console.log(objectVar, typeof objectVar); // { name: "Subhranil", age: 21 } 'object'
console.log(arrayVar, typeof arrayVar); // [1, 2, 3, 4, 5] 'object'
