// Create variables of different data types (number, string, boolean, object, array) and log each variable's type using the `typeof` operator

const x = 5; // declare x with value 5

const str = "Hello World"; // declare str with value "Hello World"

const isTrue = true; // declare isTrue with value true

const obj = {
    name: "Subhranil",
    age: 21,
    isStudent: true
} // declare obj with values

const arr = [1, 2, 3, 4, 5]; // declare arr with value [1, 2, 3, 4, 5]

console.log(typeof x); // prints number

console.log(typeof str); // prints string

console.log(typeof isTrue); // prints boolean

console.log(typeof obj); // prints object

console.log(typeof arr); // prints object
