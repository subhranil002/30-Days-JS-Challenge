// Write a script that performs basic arithmetic operations (addition, subtraction, multiplication, division, remainder) on two numbers and logs the results

const num1 = 10;
const num2 = 5;

console.log("Addition: " + (num1 + num2)); // Output: Addition: 15
console.log("Subtraction: " + (num1 - num2)); // Output: Subtraction: 5
console.log("Multiplication: " + (num1 * num2)); // Output: Multiplication: 50
console.log("Division: " + (num1 / num2)); // Output: Division: 2
console.log("Remainder: " + (num1 % num2)); // Output: Remainder: 0

