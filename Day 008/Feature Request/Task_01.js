// Write a script that demonstrates the use of template literals to create and log strings with embedded variables and multi-line strings

const name = 'Subhranil';
const age = 21;

console.log(`My name is ${name} and I am ${age} years old.`); // My name is Subhranil and I am 21 years old.

const message = `
Hello, my name is ${name} and I am ${age} years old.
I am a programmer and I love to code.
`;

console.log(message); 
// Hello, my name is Subhranil and I am 21 years old.
// I am a programmer and I love to code.
